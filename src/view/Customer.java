    void autoIDGen(){  
        try {
                Connection con = DBConnection.getInstance().getConnection();
            PreparedStatement pstm = con.prepareStatement("select cid from customer order by cid desc limit 1");
            ResultSet rs = pstm.executeQuery();
            String id = null;
            while (rs.next()) {
                id=rs.getString(1);
            }
                id = id.replaceAll("[^0-9]", "");
                int res=Integer.parseInt(id)+1;
                id="C"+String.format("%04d", res); 
                txt_id.setText(id);
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    private void getAllCutomers() {
        try {

            dtm.setRowCount(0);
            ArrayList<CustomerDTO> allCustomers = CustomerController.getAllCustomers();
            for (CustomerDTO cus : allCustomers) {
                Object[] rowData = {cus.getCid(), cus.getName(), cus.getTel()};
                dtm.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void clearAll() {
        autoIDGen();
        txt_name.setText("");
        txt_tel.setText("");
        txt_name.requestFocus();
    }
