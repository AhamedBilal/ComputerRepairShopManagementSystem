    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        try {
            String customerID = txt_id.getText();
            String customerName = txt_name.getText();
            String customerTel = txt_tel.getText();

            CustomerDTO cusModel = new CustomerDTO(customerID, customerName, customerTel);
            boolean addCustomer = CustomerController.addCustomer(cusModel);
            if (addCustomer) {
                JOptionPane.showMessageDialog(this, "Aded");
                clearAll();
                getAllCutomers();
            } else {
                JOptionPane.showMessageDialog(this, "Falied..!");
            }

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_addActionPerformed

